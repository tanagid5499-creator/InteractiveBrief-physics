/* ============================================================================
   build-registry.mjs — Clicker Phase 2A
   Extracts every Bloom .quick-quiz block from the 261111 lab pages and emits:
     • questions.js  → window.CLICKER_COURSES (registry, NO answers; loaded by host + respond)
     • answers.js    → window.CLICKER_ANSWERS (teacher-only key; loaded by host only)
   One-time content tool (run from the clicker/ folder): `node build-registry.mjs`
   Re-run whenever a lab's quiz questions change. Not part of serving (no build step).
   ========================================================================== */
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const root = join(here, "..");

const COURSE = "261111";
const COURSE_TITLE = "ปฏิบัติการฟิสิกส์ 1";

// 10 labs in book order → [folder, labKey, title]
const LABS = [
  ["261111-lab-01-measurement", "lab01", "การวัดและเครื่องมือวัด"],
  ["261111-lab-02-youngs-modulus", "lab02", "โมดูลัสของยัง"],
  ["261111-lab-03-standing-waves-string", "lab03", "คลื่นนิ่งในเส้นเชือก"],
  ["261111-lab-04-reflection-refraction", "lab04", "การสะท้อนและการหักเหของแสง"],
  ["261111-lab-05-interference-diffraction", "lab05", "การแทรกสอดและการเลี้ยวเบนของแสง"],
  ["261111-lab-06-density-specific-gravity-bottle", "lab06", "ความหนาแน่นจากขวดถ่วงจำเพาะ"],
  ["261111-lab-07-free-fall-newtons-second-law", "lab07", "วัตถุตกอิสระและกฎข้อที่สองของนิวตัน"],
  ["261111-lab-08-electrical-equivalent-heat", "lab08", "สมมูลไฟฟ้า-ความร้อน"],
  ["261111-lab-09-force", "lab09", "แรง"],
  ["261111-lab-10-pendulum-and-ball-motion", "lab10", "ลูกตุ้มนาฬิกาและลูกตุ้มฟิสิกัล"],
];

const clean = (html) =>
  html
    .replace(/<b class="qo-key">[A-E]<\/b>/g, "") // strip the A–E key badge (clicker renders its own)
    .replace(/\s+/g, " ")
    .trim();

function extractQuizzes(html) {
  // split on each quiz block; first chunk is preamble
  const chunks = html.split('<div class="quick-quiz"');
  const out = [];
  for (let i = 1; i < chunks.length; i++) {
    const seg = chunks[i];
    const head = seg.slice(0, 400);
    const qid = (head.match(/data-qid="([^"]+)"/) || [])[1];
    const bloom = (head.match(/data-bloom="([^"]+)"/) || [])[1];
    const answer = (head.match(/data-answer="([^"]+)"/) || [])[1];
    // stem = first heading in the block (markup varies: <h3 class="prompt-q"> or <h4>)
    const stem = (seg.match(/<(h[1-6])[^>]*>([\s\S]*?)<\/\1>/) || [])[2];
    const options = {};
    const reBtn = /<button[^>]*data-quiz-option="([A-E])"[^>]*>([\s\S]*?)<\/button>/g;
    let m;
    while ((m = reBtn.exec(seg))) options[m[1]] = clean(m[2]);
    if (!qid || !bloom || !answer || !stem) {
      throw new Error(`Malformed quiz block #${i}: qid=${qid} bloom=${bloom} answer=${answer}`);
    }
    out.push({ qid, bloom, answer, prompt: clean(stem), options });
  }
  return out;
}

const courses = { [COURSE]: { title: COURSE_TITLE, labs: {} } };
const answers = {};
const ids = new Set();
let total = 0;

for (const [folder, labKey, title] of LABS) {
  const file = join(root, folder, "interactive-lab", "index.html");
  const html = readFileSync(file, "utf8");
  const quizzes = extractQuizzes(html);
  const questions = quizzes.map((q) => {
    const id = `${labKey}-${q.qid}`;
    if (ids.has(id)) throw new Error(`Duplicate id ${id}`);
    ids.add(id);
    answers[id] = q.answer;
    total++;
    return { id, bloom: q.bloom, prompt: q.prompt, options: q.options };
  });
  courses[COURSE].labs[labKey] = { title, questions };
  console.log(`${labKey}  ${String(questions.length).padStart(2)} q  ·  ${title}`);
}

console.log(`\nTOTAL: ${total} questions · ${ids.size} unique ids`);

const header = (extra) =>
  `/* ============================================================================
   ${extra}
   GENERATED by build-registry.mjs — do not edit by hand.
   Re-run \`node build-registry.mjs\` from clicker/ after changing lab quiz content.
   ========================================================================== */\n`;

writeFileSync(
  join(here, "questions.js"),
  header("Clicker question registry · 261111 (NO answers — safe for student page)") +
    "window.CLICKER_COURSES = " +
    JSON.stringify(courses, null, 2) +
    ";\n",
  "utf8"
);

writeFileSync(
  join(here, "answers.js"),
  header("Clicker answer key · TEACHER-ONLY — never load on respond.html (student page)") +
    "window.CLICKER_ANSWERS = " +
    JSON.stringify(answers, null, 2) +
    ";\n",
  "utf8"
);

console.log("\nWrote questions.js + answers.js");
